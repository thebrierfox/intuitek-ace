def generate_and_deliver(
    customer: dict,
    db_path: str,
    fernet: Fernet,
    resend_api_key: str,
) -> None:
    """
    customer dict keys: id, name, email, agent_name, use_case, license_key
    Retrieves encrypted API key from DB, decrypts in-scope only.
    """
    # Retrieve + decrypt API key — in-scope only
    api_key_enc = _fetch_api_key_enc(db_path, customer["id"])
    anthropic_api_key = fernet.decrypt(api_key_enc.encode()).decode()

    # Summarize use case to 3 sentences for SOUL.md
    use_case_summary = _summarize_use_case(customer["use_case"])

    files = {
        "SOUL.md":           _gen_soul(customer, use_case_summary),
        "HEARTBEAT.md":      _gen_heartbeat(customer),
        "MEMORY.md":         _gen_memory(customer, use_case_summary),
        "docker-compose.yml": _gen_docker_compose(customer, anthropic_api_key),
        "SETUP.md":          _gen_setup(customer),
        "TELEGRAM_SETUP.md": _gen_telegram_setup(customer),
        "DISCORD_INVITE.md": _gen_discord_invite(),
    }

    # Explicitly scrub key from scope
    del anthropic_api_key

    zip_bytes = _build_zip(files)
    _send_delivery_email(customer, zip_bytes, resend_api_key)

    log.info("Package delivered to %s (%s)", customer["email"], customer["agent_name"])


# ── DATABASE ─────────────────────────────────────────────────
def _fetch_api_key_enc(db_path: str, customer_id: str) -> str:
    conn = sqlite3.connect(db_path)
    row = conn.execute(
        "SELECT api_key_enc FROM customers WHERE id = ?", (customer_id,)
    ).fetchone()
    conn.close()
    if not row or not row[0]:
        raise ValueError(f"No encrypted API key for customer {customer_id}")
    return row[0]


# ── USE CASE SUMMARIZATION ───────────────────────────────────
def _summarize_use_case(raw: str) -> str:
    """
    Condense free-text use_case_description to 3 clean sentences.
    For v1.0, uses a rule-based truncation approach.
    In v1.1: wire to claude-haiku-4-5 for actual summarization.
    """
    sentences = [s.strip() for s in raw.replace("\n", " ").split(".") if s.strip()]
    selected  = sentences[:3]
    summary   = ". ".join(selected)
    if summary and not summary.endswith("."):
        summary += "."
    return summary or raw[:400]


