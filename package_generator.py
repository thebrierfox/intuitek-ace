# ── ZIP BUILDER ──────────────────────────────────────────────
def _build_zip(files: dict[str, str]) -> bytes:
    buf = io.BytesIO()
    with zipfile.ZipFile(buf, "w", zipfile.ZIP_DEFLATED) as zf:
        for filename, content in files.items():
            zf.writestr(filename, content)
    return buf.getvalue()


# ── EMAIL DELIVERY ───────────────────────────────────────────
def _send_delivery_email(
    customer: dict,
    zip_bytes: bytes,
    resend_api_key: str,
) -> None:
    agent_name = customer["agent_name"]
    first_name = customer["name"].split()[0]

    zip_b64 = base64.b64encode(zip_bytes).decode()

    payload = {
        "from": "agent@intuitek.ai",
        "to": [customer["email"]],
        "subject": f"Your agent {agent_name} is ready — setup files enclosed",
        "text": textwrap.dedent(f"""\
            {first_name},

            Your IntuiTek¹ agent {agent_name} has been generated and is ready to deploy.

            Open the attached ZIP file. Start with SETUP.md — it walks you through
            the complete deployment in 10 steps. Total time: ~15 minutes.

            What you need:
            - A Contabo VPS (~$6.99/month at contabo.com) — you may already have one
            - Your Telegram app (to communicate with your agent)
            - About 15 minutes

            Your license key: {customer['license_key']}
            (Already pre-filled in your docker-compose.yml)

            Community: {DISCORD_INVITE}
            Support: {INTUITEK_SITE}

            — IntuiTek¹
            {INTUITEK_SITE}
        """),
        "attachments": [
            {
                "filename": f"{agent_name.lower().replace(' ', '_')}_agent_package.zip",
                "content": zip_b64,
            }
        ],
    }

    resp = httpx.post(
        "https://api.resend.com/emails",
        headers={"Authorization": f"Bearer {resend_api_key}"},
        json=payload,
        timeout=30,
    )
    if resp.status_code not in (200, 201):
        raise RuntimeError(f"Resend delivery error {resp.status_code}: {resp.text}")
